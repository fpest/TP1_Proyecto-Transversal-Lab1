    public void llenarListaAlumnos() {
        if (txtAlumno.getText() != "") {
            listaAlumnos = (ArrayList) alumnoData.obtenerAlumnos(txtAlumno.getText());
        } else {
            listaAlumnos = (ArrayList) alumnoData.obtenerAlumnos();
        }

        lstAlumnos.removeAll();

        int largoLista = listaAlumnos.size();
        //    String[] apellidoNombre = new String[largoLista];
        Alumno[] apellidoNombre = new Alumno[largoLista];
        int i = 0;

        for (Alumno alumno : listaAlumnos) {
            if (alumno.isActivo()) {
                apellidoNombre[i] = alumno;
                i++;
            }
        }

        lstAlumnos.setModel(new javax.swing.AbstractListModel<Alumno>() {
            public int getSize() {
                return listaAlumnos.size();
            }

            public Alumno getElementAt(int i) {
                return apellidoNombre[i];
            }
        });

    }

    public void llenarListaMaterias() {
        if (txtMateria.getText() != "") {
            listaMaterias = (ArrayList) materiaData.obtenerMateria(txtMateria.getText());
        } else {
            listaMaterias = (ArrayList) materiaData.obtenerMateria();
        }

        lstMaterias.removeAll();

        int largoLista = listaMaterias.size();
        //    String[] apellidoNombre = new String[largoLista];
        Materia[] nombre = new Materia[largoLista];
        int i = 0;

        for (Materia materia : listaMaterias) {
            if (materia.isActivo()) {
                nombre[i] = materia;
                i++;
            }
        }

        lstMaterias.setModel(new javax.swing.AbstractListModel<Materia>() {
            public int getSize() {
                return listaMaterias.size();
            }

            public Materia getElementAt(int i) {
                return nombre[i];
            }
        });

    }

