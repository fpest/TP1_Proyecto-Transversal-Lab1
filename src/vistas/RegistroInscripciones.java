    private void btnInscribirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInscribirActionPerformed

        Inscripcion inscripcion = new Inscripcion(alumno, materia);

        if (inscripcionData.inscribir(inscripcion)) {
            JOptionPane.showMessageDialog(null, "Inscripcion Registrada correctamente.");
        } else {
            
          inscripcion = inscripcionData.buscarInscripcion(alumno, materia);
            
            if (inscripcion.isActivo()) {
                JOptionPane.showMessageDialog(null, "No se pudo guardar la información. No se puede inscribir el mismo alumno dos veces en la misma Materia.");
            } else {
                if (inscripcionData.reInscribir(alumno, materia)) {
                    JOptionPane.showMessageDialog(null, "Se reinscribió el Alumno en la Materia.");
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo reinscribir el Alumno en la Materia.");
                }
            }

        }


    }//GEN-LAST:event_btnInscribirActionPerformed

